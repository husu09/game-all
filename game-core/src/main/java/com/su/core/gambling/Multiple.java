package com.su.core.gambling;



public class Multiple {
	private int value;
	private int type;

	public Multiple(int type, int value) {
		this.type = type;
		this.value = value;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public int getType() {
		return type;
	}


}
